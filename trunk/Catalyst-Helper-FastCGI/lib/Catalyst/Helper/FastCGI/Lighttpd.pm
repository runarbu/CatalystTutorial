package Catalyst::Helper::FastCGI::Lighttpd;
use warnings;
use strict;

=head1 NAME

Catalyst::Helper::FastCGI::Lighttpd

=head1 SYNOPSIS

Helper module for finding FastCGI deployment issues under Lighttpd.
At present this module does nothing except provide tests for problems
with FastCGI deployment under lighttpd.

=head1 DESCRIPTION

This module exists to provide a platform for testing deployment issues
for Catalyst running under FastCGI with Lighttpd.  It doesn't actually
do anything except provide a platform for distributing tests with
which to iron out issues.

=cut

1;
